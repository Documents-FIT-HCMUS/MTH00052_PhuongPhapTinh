clear all
clc
format longG

syms x
f = 6*pi*x^2*(6-x);
xx = [ 0; 0.2; 0.4; 0.6; 0.8; 1; 1.2 ];
yy = subs(f,x,xx);
h = xx(2:end)-xx(1:end-1);
VT = [ 1, 0, 0, 0, 0, 0, 0; h(1)/6, (h(1)+h(2))/3, h(2)/6, 0, 0, 0, 0;
        0, h(2)/6, (h(2)+h(3))/3, h(3)/6, 0, 0, 0; 0, 0, h(3)/6, (h(3)+h(4))/3, h(4)/6, 0, 0;
        0, 0, 0, h(4)/6, (h(4)+h(5))/3, h(5)/6, 0; 0, 0, 0, 0, h(5)/6, (h(5)+h(6))/3, h(6)/6;
        0, 0, 0, 0, 0, 0, 1 ]
VP = [ 0; (yy(3)-yy(2))/h(2) - (yy(2)-yy(1))/h(1); 
    (yy(4)-yy(3))/h(3) - (yy(3)-yy(2))/h(2); (yy(5)-yy(4))/h(4) - (yy(4)-yy(2))/h(3); (yy(6)-yy(5))/h(5) - (yy(5)-yy(4))/h(4);
    (yy(7)-yy(6))/h(6) - (yy(6)-yy(5))/h(5); 0 ];
m = inv(VT)*VP;
M = yy(1:end-1) - m(1:end-1).*h(1:end).^2/6;
N = yy(2:end) - m(2:end).*h(1:end).^2/6;
S1 = m(2)*(x-xx(1))^3/6/h(1) + m(1)*(xx(2)-x)^3/6/h(1) + M(1)*(xx(2)-x)/h(1) + N(1)*(x-xx(1))/h(1);
S2 = m(3)*(x-xx(2))^3/6/h(2) + m(2)*(xx(3)-x)^3/6/h(2) + M(2)*(xx(3)-x)/h(2) + N(2)*(x-xx(2))/h(2);
S3 = m(4)*(x-xx(3))^3/6/h(3) + m(3)*(xx(4)-x)^3/6/h(3) + M(3)*(xx(4)-x)/h(3) + N(3)*(x-xx(3))/h(3);
S4 = m(5)*(x-xx(4))^3/6/h(4) + m(4)*(xx(5)-x)^3/6/h(4) + M(4)*(xx(5)-x)/h(4) + N(4)*(x-xx(4))/h(4);
S5 = m(6)*(x-xx(5))^3/6/h(5) + m(5)*(xx(6)-x)^3/6/h(5) + M(5)*(xx(6)-x)/h(5) + N(5)*(x-xx(5))/h(5);
S6 = m(7)*(x-xx(6))^3/6/h(6) + m(6)*(xx(7)-x)^3/6/h(6) + M(6)*(xx(7)-x)/h(6) + N(6)*(x-xx(6))/h(6);

hold on;
plot(xx, yy, 'bo');
fplot(S1, [xx(1) xx(2)]);
fplot(S2, [xx(2) xx(3)]);
fplot(S3, [xx(3) xx(4)]);
fplot(S4, [xx(4) xx(5)]);
fplot(S5, [xx(5) xx(6)]);
fplot(S6, [xx(6) xx(7)]);